# Operadores aritméticos
a = 10
b = 5

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
resto = a % b
exponenciacao = a ** b

print("Soma:", soma)
print("Subtração:", subtracao)
print("Multiplicação:", multiplicacao)
print("Divisão:", divisao)
print("Resto:", resto)
print("Exponenciação:", exponenciacao)

# Operadores de atribuição
x = 10
x += 5  # x = x + 5
x -= 3  # x = x - 3
x *= 2  # x = x * 2
x /= 4  # x = x / 4
x %= 3  # x = x % 3
x **= 2  # x = x ** 2

print("Valor de x:", x)

# Operadores de comparação
a = 10
b = 5

print("a > b:", a > b)
print("a < b:", a < b)
print("a == b:", a == b)
print("a != b:", a != b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)

# Operadores lógicos
x = True
y = False

print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)
