# Verificação de temperatura
temperatura = 30

if temperatura < 0:
    print("Está congelando lá fora!")
elif temperatura < 20:
    print("Está frio lá fora.")
elif temperatura < 30:
    print("Está agradável lá fora.")
elif temperatura < 40:
    print("Está quente lá fora!")
else:
    print("Está muito quente lá fora!!!")
